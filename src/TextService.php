    public static function _AccentedCharTransform($input)
    {
        $search = explode(',', 'ç,æ,œ,á,é,í,ó,ú,à,è,ì,ò,ù,ä,ë,ï,ö,ü,ÿ,â,ê,î,ô,û,å,ø,u');
        $replace = explode(',', 'c,ae,oe,a,e,i,o,u,a,e,i,o,u,a,e,i,o,u,y,a,e,i,o,u,a,o,u');
        foreach ($search as $value) {
            $supper[] = strtoupper($value);
        }
        foreach ($replace as $value) {
            $rupper[] = strtoupper($value);
        }
        $search = array_merge($search, $supper);
        $replace = array_merge($replace, $rupper);

        $output = str_replace($search, $replace, $input);
        return $output;
    }

    // Function to check the string is ends
    // with given substring or not
    public static function _EndsWith($string, $endString)
    {
        $len = strlen($endString);
        if ($len == 0) {
            return true;
        }
        return (substr($string, -$len) === $endString);
    }

    // Function to check string starting
    // with given substring
    public static function _StartsWith($string, $startString)
    {
        $len = strlen($startString);
        return (substr($string, 0, $len) === $startString);
    }

    /**
     * Restituisce una stringa conenente numeri e lettere casuali
     *
     * @param int|null $num dimensione della stringa restituita
     * @param string|null $crypt tipo di funzione di crittografia
     * @return string
     */
    public static function _getCasuale(int $dim = NULL, string $crypt = null, bool $number = false)
    {
        $dim = (NULL === $dim) ? 16 : $dim;
        if (false === $number) {
            $str = 'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz1234567890';
        } else {
            $str = '0123456789';
        }
        $re_handle = '';
        for ($i = 0; $i <= $dim; $i++) {
            $re_handle .= substr($str, rand(0, strlen($str) - 1), 1);
        }
        return (null !== $crypt) ? $crypt($re_handle) : $re_handle;
    }

